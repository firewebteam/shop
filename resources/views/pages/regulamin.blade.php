</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Zamówione Towary dostarczane są zgodnie z wyborem Klienta bądź to bezpośrednio na adres Klienta wskazany w internetowym formularzu składanego zamówienia i potwierdzonego przez Klienta, jako adres do wysyłki lub odbierane osobiście w punkcie odbioru osobistego pod adresem podanym w trakcie wykonywania zamówienia. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Towar zawsze zapakowany jest w sposób odpowiadający jego właściwościom, tak aby nie ulegał podczas transportu uszkodzeniom, utracie bądź zniszczeniu.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Klient na bieżąco jest informowany o kosztach dostawy, są one podawane w trakcie wypełniania przez Klienta internetowego formularza zamówienia. Wysokość kosztów wysyłki zależy od kraju do którego wysyłane jest zamówienie, ilości zamawianych towarów ich wagi oraz sposobu nadania przesyłki.</span></p>
</li>
</ol>
