<p align="JUSTIFY"><span style="color: #000000;">Czas realizacji zamówienia dla pojedynczego Klienta jest określony przy każdym produkcie oraz w podsumowaniu zamówienia po dodaniu do koszyka wszystkich produktów</span><span style="color: #272727;">. W przypadku produktów oznaczonych dostępnością ‘na zamówienie’ czas dostawy określany jest na stronie produktu</span><span style="color: #000000;">. Na czas realizacji zamówienia składa się przede wszystkim czas przygotowania zamówienia (kompletowanie oraz pakowanie zamówienia, wydanie przesyłki kurierowi, a w wybranych przypadkach wykonanie Towaru). Czas dostawy zamówienia zależny jest od wybranego sposobu doręczenia, może on się zmieniać w zależności od rodzaju wskazanego przez Klienta środka transportu. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">W przypadku zaistnienia wyjątkowych okoliczności lub braku możliwości realizacji zamówienia we wskazanym w pkt. 2 terminie, Sprzedawca niezwłocznie kontaktuje się z Klientem w celu ustalenia dalszego trybu postępowania w tym ustalenia innego terminu realizacji zamówienia, zmiany sposobu doręczenia.</span></p>
</li>
</ol>
