<p align="CENTER"><span style="color: #000000;">§10 Postępowanie reklamacyjne</span></p>
<p align="JUSTIFY"> </p>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Dla prawidłowego złożenia reklamacji Klient powinien podać swoje dane takie jak: imię i nazwisko lub nazwę firmy, adres zamieszkania lub adres siedziby firmy oraz adres poczty elektronicznej, przedmiot reklamacji, w miarę możliwości numer zamówienia wraz ze wskazaniem okresu czasu, którego dotyczy reklamacja oraz okoliczności uzasadniające złożenie reklamacji (opis na czym ona polega) ewentualnie jakich cech Towar zamawiany nie posiada, a według zapewnień Sprzedawcy lub według sposobu przedstawienia go Klientowi miał posiadać.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Jeżeli Klientem jest Konsument, może zażądać wymiany Towaru na taki, który jest wolny od wad zamiast zaproponowanego przez Sprzedawcę, usunięcia wady albo zamiast wymiany Produktu żądać usunięcia wady, chyba że doprowadzenie rzeczy do zgodności z umową w sposób wybrany przez Klienta jest niemożliwe albo wymagałoby nadmiernych kosztów w porównaniu ze sposobem proponowanym przez Sprzedawcę. Przy ocenie nadmierności kosztów uwzględnia się wartość rzeczy wolnej od wad, rodzaj i znaczenie stwierdzonej wady, a także bierze się pod uwagę niedogodności, na jakie narażałby Klienta inny sposób zaspokojenia. </span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk489514436"></a> <span style="color: #000000;">Jeżeli przepisy odrębne nie stanowią inaczej, przedsiębiorca jest obowiązany udzielić odpowiedzi na reklamację konsumenta w terminie 14 dni od dnia jej otrzymania. Jeżeli przedsiębiorca nie udzielił odpowiedzi na reklamację w terminie, o którym mowa wyżej , uważa się, że uznał reklamację. Odpowiedź na reklamację przedsiębiorca przekazuje konsumentowi na papierze lub innym trwałym nośniku (np. pamięci USB lub płycie CD/DVD, odpowiadając na reklamację.)"</span><span style="color: #000000;"> </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">W przypadku nierozpatrzenia zgłoszenia w zakreślonym terminie, należy uznać je za uwzględnione przez Sprzedawcę. Roszczenie dotyczące odstąpienia od umowy, w przypadku jego nierozpatrzenia w terminie, nie jest równoznaczne z uznaniem złożonej reklamacji</span></p>
</li>
</ol>
