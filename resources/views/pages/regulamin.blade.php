<p align="CENTER"><span style="color: #000000;">§ 12 Pozasądowe sposoby rozpatrywania reklamacji i dochodzenia roszczeń</span></p>
<p align="CENTER"> </p>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Informacje o pozasądowych sposobach rozpatrywania reklamacji i dochodzenia roszczeń, a także zasady dostępu do tych procedur udostępniane są w siedzibach oraz na stronach internetowych powiatowych (miejskich) rzeczników Konsumentów, organizacji społecznych, do których zadań statutowych należy ochrona Konsumentów, Wojewódzkich Inspektoratów Inspekcji Handlowej oraz pod następującymi adresami Urzędu Ochrony Konkurencji i Konsumentów: www.uokik.gov.pl/spory_konsumenckie.php,www.uokik.gov.pl/wazne_adresy.php, </span><span style="color: #0563c1;"><span lang="zxx"><u><a class="western" href="http://www.uokik.gov.pl/sprawy_indywidualne.php"><span style="color: #000000;">www.uokik.gov.pl/sprawy_indywidualne.php</span></a></u></span></span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Konsument posiada między innymi następujące możliwości skorzystania z pozasądowych sposobów rozpatrywania reklamacji i dochodzenia swoich roszczeń:</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zwrócenie się do Wojewódzkiego Inspektora Inspekcji Handlowej z wnioskiem o wszczęcie postępowania mediacyjnego w sprawie polubownego zakończenia sporu.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zwrócenie się do stałego polubownego Sądu Konsumenckiego działającego przy Wojewódzkim Inspektorze Inspekcji Handlowej z wnioskiem o rozstrzygnięcie sporu wynikłego z zawartej umowy, adres www.uokik.gov.pl/wazne_adresy.php.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zwrócenie się o bezpłatną pomoc prawną m.in. do Federacji Konsumentów - adres strony internetowej: </span><span style="color: #0563c1;"><span lang="zxx"><u><a class="western" href="http://www.federacjakonsumentow.org.pl/"><span style="color: #000000;">www.federacjakonsumentow.org.pl</span></a></u></span></span><span style="color: #000000;">.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">W rozwiązywaniu sporów transgranicznych pomaga Sieć Europejskich Centrów Konsumenckich. Adresy tych instytucji dostępne są na stronie internetowej Europejskiego Centrum Konsumenckiego </span><span style="color: #0563c1;"><span lang="zxx"><u><a class="western" href="http://www.konsument.gov.pl/"><span style="color: #000000;">www.konsument.gov.pl</span></a></u></span></span><span style="color: #000000;">.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Konsument może również skorzystać z platformy internetowego systemu rozstrzygania sporów (platforma ODR), zgodnie z rozporządzeniem Parlamentu Europejskiego i Rady (UE) Nr 524/2013 z dnia 21 maja 2013 r. w sprawie internetowego systemu rozstrzygania sporów Konsumenckich oraz zmiany rozporządzenia (WE) nr 2006/2004 i dyrektywy 2009/22/WE (rozporządzenie w sprawie ODR w sporach konsumenckich). ODR (online dispute resolution) dostępnej pod adresem elektronicznym: http://ec.europa.eu/consumers/odr/ Europejska platforma ODR stanowi jeden wspólny punkt dostępu dla konsumentów i przedsiębiorców, umożliwiający pozasądowe rozstrzyganie sporów dotyczących zobowiązań umownych, wynikających z zawartej internetowej umowy sprzedaży: </span><span style="color: #0563c1;"><span lang="zxx"><u><a class="western" href="https://webgate.ec.europa.eu/odr/main/index.cfm?event=main.home.show&amp;lng=PL" target="_blank"><span style="color: #000000;">https://webgate.ec.europa.eu/odr/main/index.cfm?event=main.home.show&amp;lng=PL</span></a></u></span></span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Skorzystanie z pozasądowych sposobów rozpatrywania reklamacji i dochodzenia roszczeń ma charakter dobrowolny i może mieć miejsce tylko i wyłącznie gdy obie strony sporu tj Sprzedawca i Klient wyrażą na to zgodę.</span></p>
</li>
</ol>
<p align="JUSTIFY"> </p>
<p align="CENTER"><span style="color: #000000;">§13 Postanowienia końcowe</span></p>
<p align="JUSTIFY"> </p>
<ol>
<li>
<p align="JUSTIFY"><a name="_Hlk489514036"></a> <span style="color: #000000;">Sklep internetowy honoruje wszelkie prawa Klientów przewidziane w przepisach obowiązującego prawa. </span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk489514093"></a> <span style="color: #000000;">Jeśli obowiązujące prawo przyznaje Klientom będącym konsumentami korzystniejsze obowiązkowe i wymagane prawem uregulowania niż te, które są zawarte w niniejszym Regulaminie, odpowiednie postanowienia Regulaminu są bezpośrednio zastępowane przez konkretne normy obowiązującego prawa i tym samym są wiążące dla w/w właściciela.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Wszelkie treści zamieszczone na stronie Sklepu internetowego (wliczając w to grafikę, teksty, układ stron i logotypy) korzystają z ochrony przewidzianej dla praw autorskich i są wyłączną własnością Sprzedawcy. Wykorzystywanie tych treści bez pisemnej zgody Sprzedawcy skutkuje odpowiedzialnością cywilną oraz karną.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Właściciel sklepu, jako administrator danych osobowych, informują Pana/ Panią, iż:</span></p>
</li>
</ol>
<ul>
<li>
<p align="JUSTIFY"><span style="color: #000000;">podanie danych jest zawsze dobrowolne ale niezbędne do realizacji zamówienia;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">osoba podająca swoje dane osobowe ma nieograniczone prawo dostępu do wszystkich treści swoich danych i ich sprostowania, usunięcia (prawo do bycia zapomnianym), ograniczenia przetwarzania, prawo do przenoszenia danych, prawo do cofnięcia zgody w dowolnym momencie bez wpływu na zgodność z prawem przetwarzania, dane mogą być udostępnione jednak właściwym organom państwa w sytuacji gdy odpowiedni przepis tego będzie wymagał. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Podstawą przetwarzania danych osobowych będzie art. 6 ust. 1 pkt a) oraz treść ogólnego rozporządzenia o ochronie danych.;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">dane osobowe będą przechowywane i przetwarzane przez okres niezbędny do zakończenia przetwarzania i realizacji zamówienia lecz nie dłużej niż przez okres 3 lat (2 lata okres to okres reklamacji i 1 rok na ewentualnie inne roszczenia i sytuację wyjątkowe)</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">osoba udostępniająca swoje dane osobowe ma prawo wniesienia skargi do UODO gdy uzna że przetwarzanie danych osobowych dotyczących realizacji zamówienia narusza przepisy ogólnego rozporządzenia o ochronie danych osobowych z dnia 27 kwietnia 2016 r.;”</span></p>
</li>
</ul>
<ol start="5">
<li>
<p align="JUSTIFY"><span style="color: #000000;">W zakresie przetwarzania danych osobowych niniejszego sklepu nie został stwierdzony przez Komisję Europejską odpowiedni stopień ochrony w drodze decyzji ale dane będą odpowiednio zabezpieczone za pomocą rozwiązań i środków informatyczno/prawnych.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Państwa dane będą przetwarzane w sposób zautomatyzowany w tym również w formie profilowania.</span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk489514124"></a><a name="_Hlk491076942"></a> <span style="color: #000000;">W pozostałych kwestiach nieuregulowanych zapisami niniejszego Regulaminu mają zastosowanie odpowiednie przepisy prawa polskiego</span><span style="color: #000000;">, w szczególności: </span></p>
</li>
</ol>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 2 marca 2000 roku o ochronie niektórych praw konsumentów oraz odpowiedzialności za szkodę wyrządzoną przez produkt niebezpieczny (Dz.U. 2000 Nr 22 poz. 271 ze zm.).</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 27 lipca 2002 r. o szczególnych warunkach sprzedaży konsumenckiej oraz o zmianie Kodeksu cywilnego (Dz.U. 2002 Nr 141, poz. 1176 ze zm.);</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 23 kwietnia 1964 r. Kodeks Cywilny (Dz.U. 1964 nr 16 poz. 93 ze zm.);</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 18 lipca 2002 r. o świadczeniu usług drogą elektroniczną (Dz.U. 2013 poz. 1422);</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 30 czerwca 2000 r. Prawo własności przemysłowej (Dz.U. 2001 Nr 49 poz. 508 ze zm.);</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 4 lutego 1994 r. o prawie autorskim i prawach pokrewnych (Dz.U. 2006 Nr 90 poz. 631 ze zm.),</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Ustawa z dnia 30 maja 2014 r. o prawach konsumenta (Dz. U. 2014, poz. 827)</span></p>
</li>
</ol>
<p align="JUSTIFY"> </p>
<ol start="8">
<li>
<p align="JUSTIFY"><span style="color: #000000;">Zmieniony Regulamin wiąże Klientów jeżeli zostały zachowane wymagania określone w art. 384 Kodeksu cywilnego (tj. Klient został prawidłowo powiadomiony o zmianach).</span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk491076956"></a> <span style="color: #000000;">Sprzedawca zastrzega sobie prawo do dokonywania zmian Regulaminu z ważnych przyczyn to jest: </span></p>
</li>
</ol>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zmiany przepisów prawa; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zmiany sposobów płatności i dostaw; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zmiana kursu walut, </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zmiany sposobu świadczenia usług drogą elektroniczną objętym regulaminem, </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zmiany danych Sprzedawcy, w tym adresu poczty elektronicznej, numeru telefonu. </span></p>
</li>
</ol>
<ol start="10">
<li>
<p align="JUSTIFY"><span style="color: #000000;">Zmiany regulaminu nie mają wpływu na złożone i realizowane już zamówienia, do nich zastosowanie ma regulamin obowiązującym w chwili złożenia zamówienia. Sprzedawca o zamierzonej zmianie informuje na stronie sklepu co najmniej na 30 dni wcześniej. W przypadku braku akceptacji zmienionego regulaminu Usługobiorcy mogą w terminie 30 dni od dnia otrzymania wiadomości wypowiedzieć umowę ze skutkiem natychmiastowym. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Spory powstałe w wyniku świadczenia usług na podstawie niniejszego Regulaminu zostaną poddane pod rozstrzygnięcie Sądowi Powszechnemu według wyboru Klienta będącego jednocześnie konsumentem, zgodnie z właściwymi przepisami prawa polskiego.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Załączniki do Regulaminu stanowią jego integralną część.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Umowa sprzedaży zawierana jest w języku polskim, o treści zgodnej z Regulaminem.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Klienci w/w sklepu mogą uzyskać dostęp do niniejszego Regulaminu w każdym czasie za pośrednictwem odsyłacza zamieszczonego na stronie głównej serwisu oraz pobrać go i sporządzić jego wydruk, komercyjne wykorzystanie podlega jednak ochronie Kancelarii Prawnej LEGATO. </span></p>
</li>
