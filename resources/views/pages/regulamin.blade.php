<li>
<p align="JUSTIFY"><span style="color: #000000;">Zabronione jest zamieszczanie przez Klient treści które mogłyby w szczególności:</span></p>
</li>
</ol>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">z zamiarem naruszenia dóbr osobistych osób trzecich;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zamieszczonych w złej wierze bądź takich które mogły by być za takie uznane;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">naruszać prawa osób trzecich, praw autorskich, praw pokrewnych, praw własności przemysłowej, tajemnicy przedsiębiorstwa lub objętych klauzulą poufności zwłaszcza określanych jako tajne lub ściśle tajne;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">umieszczania treści obraźliwych bądź stanowiących groźbę skierowaną do innych osób, wypowiedzi powszechnie uważanych za obraźliwe np. wulgaryzmy;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">naruszać słuszne interesy Sprzedawcy;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">rozsyłanie lub umieszczanie w ramach Sklepu Internetowego niezamówionej informacji handlowej (spam);</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">naruszać w inny sposób dobre obyczaje, przepisy obowiązującego prawa, normy społeczne lub obyczajowe.</span></p>
</li>
</ol>
<ol start="3">
<li>
<p align="JUSTIFY"><span style="color: #000000;">W przypadku otrzymania powiadomienia przez osobę trzecią, uprawnioną bądź organ władzy Państwowej Sprzedawca zastrzega sobie prawo do modyfikowania lub usuwania treści zamieszczanych przez Klient, w sytuacji stwierdzenia że mogą one stanowić naruszenie niniejszego Regulaminu lub obowiązujących przepisów prawa. Sprzedawca nie kontroluje na bieżąco zamieszczanych treści.</span></p>
</li>
</ol>
