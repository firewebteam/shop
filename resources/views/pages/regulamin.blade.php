<p align="JUSTIFY"><span style="color: #000000;">zamieszczania danych osobowych osób trzecich, rozpowszechniania wizerunku bez wymaganego zezwolenia lub zgody osoby trzeciej której dane te dotyczą;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">zamieszczania treści o charakterze reklamowym i lub promocyjnym, niezgodny z celem działalności sklepu.</span></p>
</li>
</ol>
