<p align="CENTER"><span style="color: #000000;">§9 Odstąpienie</span></p>
<p align="CENTER"> </p>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Zgodnie z przepisami prawa Klientowi będącemu Konsumentem w myśl art. 27 ustawy z dnia 30 maja 2014 r. </span><span style="color: #000000;">(Dz.U. z 2014 r. poz. 827 z późn. zm.) </span><span style="color: #000000;">o prawach Konsumenta, przysługuje prawo do odstąpienia od umowy zawartej na odległość bez podawania przyczyny. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Prawo do odstąpienia od umowy przysługuje w terminie 14 dni kalendarzowych od momentu objęcia Towaru w posiadanie przez Klienta będącego jednocześnie Konsumentem lub osobę trzecią przez niego wskazaną inną niż przewoźnik.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Gdy Klient będący Konsumentem odstąpi od umowy, umowa jest uważana za niezawartą, a Konsument jest zwolniony wówczas z wszelkich zobowiązań. To co strony świadczyły sobie nawzajem podlega zwrotowi w stanie niezmienionym, chyba że zmiana była konieczna w granicach zwykłego zarządu. Zwrot powinien nastąpić niezwłocznie, nie później niż w terminie czternastu dni.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Klient będący Konsumentem może odstąpić od umowy, składając oświadczenie na internetowym formularzu stanowiącym załącznik nr 1 do niniejszego Regulaminu, przesyłając je drogą elektroniczną lub na adres pocztowy Sprzedawcy według wyboru Klienta. Załącznik nr 1 stanowi tylko pomoc w odstąpieniu od umowy, nie jest wzorem koniecznym do skorzystania z prawa do odstąpienia od umowy. Klient może ale nie musi z niego korzystać. Dla skutecznego odstąpienia wystarczy przesłanie oświadczenia na piśmie na adres Sprzedawcy.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Do zachowania terminu określonego w pkt. 2 wystarczy wysłanie oświadczenia Klienta o odstąpieniu od umowy przed jego upływem.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Sprzedawca niezwłocznie potwierdzi Klientowi otrzymanie oświadczenia o odstąpieniu od umowy i stosownie poinformuje Klienta o dalszym postępowaniu, w tym o sposobie zwrotu Towaru oraz w razie pytań udzieli na nie odpowiedzi. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Sprzedawca niezwłocznie, w terminie nie dłuższym niż 14 dni kalendarzowych od dnia otrzymania oświadczenia Klienta o odstąpieniu od umowy, zwróci Klientowi wszelkie otrzymane od niego płatności, w tym koszty dostarczenia rzeczy. Sprzedawca dokonuje zwrotu płatności przy użyciu takiego samego sposobu płatności, jakiego użył Klient, chyba że Klient wyraźnie zgodził się na inny sposób zwrotu płatności, który nie wiąże się dla niego z żadnymi kosztami.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Jeżeli Sprzedawca po uzyskaniu zgody od Klienta nie zobowiązał się, że sam odbierze od niego Towar, Sprzedawca może wstrzymać się ze zwrotem otrzymanych płatności, w tym kosztów dostarczenia rzeczy aż do chwili otrzymania Towaru z powrotem lub dostarczenia przez Klienta dowodu/potwierdzenia jego odesłania, w zależności od tego, które zdarzenie nastąpi wcześniej.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Klient ma obowiązek zwrócić rzecz Sprzedawcy lub przekazać ją osobie upoważnionej przez Sprzedawcę niezwłocznie, jednak nie później niż w terminie 14 dni kalendarzowych, od dnia w którym odstąpił od umowy, chyba że Sprzedawca zaproponował, że sam odbierze Towar. Do zachowania terminu wystarczy odesłanie Towaru przed jego upływem.</span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk489511247"></a> <span style="color: #000000;">Klient będący Konsumentem ponosi wyłącznie bezpośrednie koszty zwrotu Towaru. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Konsumentowi, przysługuje prawo do odstąpienia od umowy zawartej na odległość, bez podania przyczyny i bez ponoszenia kosztów, z wyjątkiem kosztów określonych w art. 33, art. 34 Prawa konsumenckiego.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Towar należy dostarczyć na Adres Sprzedawcy (patrz §1 pkt 3.: </span><span style="color: #000000;">„Adres Sprzedawcy”</span><span style="color: #000000;">).</span></p>
</li>
<li>
<p align="JUSTIFY"><a name="_Hlk489511351"></a> <span style="color: #000000;">Konsument ponosi </span><strong><span style="color: #000000;">odpowiedzialność za zmniejszenie wartości rzeczy </span></strong><span style="color: #000000;">będące wynikiem korzystania z niej w sposób </span><span style="color: #000000;"><strong>wykraczający poza konieczny do stwierdzenia charakteru, cech i funkcjonowania Towaru</strong></span><span style="color: #000000;">. Oznacza to, że Kupujący ma prawo ocenić i sprawdzić Towar, ale tylko w taki sposób w jaki mógłby to uczynić w sklepie stacjonarnym (czyli sprawdzić jego kompletność i parametry techniczne). Konsument nie może bowiem normalnie użytkować rzeczy </span><strong><span style="color: #000000;">w przeciwnym razie odstępując od umowy może zostać obciążony dodatkowymi kosztami w związku ze zmniejszeniem jej wartości</span></strong><span style="color: #000000;">. </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Prawo do odstąpienia od umowy nie przysługuje Klientowi w odniesieniu do umów określonych w art. 38 ustawy z dnia 30 maja 2014 r. o prawach Konsumenta m. in. w sytuacji:</span></p>
</li>
</ol>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">o świadczenie usług, jeżeli przedsiębiorca wykonał w pełni usługę za wyraźną zgodą Konsumenta, który został poinformowany przed rozpoczęciem świadczenia, że po spełnieniu świadczenia przez przedsiębiorcę utraci prawo odstąpienia od umowy; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">w której cena lub wynagrodzenie zależy od wahań na rynku finansowym, nad którymi przedsiębiorca nie sprawuje kontroli, i które mogą wystąpić przed upływem terminu do odstąpienia od umowy; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">w której przedmiotem świadczenia jest rzecz nieprefabrykowana, wyprodukowana według specyfikacji Konsumenta lub służąca zaspokojeniu jego zindywidualizowanych potrzeb; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">w której przedmiotem świadczenia jest rzecz ulegająca szybkiemu zepsuciu lub mająca krótki termin przydatności do użycia; </span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">w której przedmiotem świadczenia jest rzecz dostarczana w zapieczętowanym opakowaniu, której po otwarciu opakowania nie można zwrócić ze względu na ochronę zdrowia lub ze względów higienicznych, jeżeli opakowanie zostało otwarte po dostarczeniu;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">w której przedmiotem świadczenia są rzeczy, które po dostarczeniu, ze względu na swój charakter zostają nierozłącznie połączone z innymi rzeczami.</span></p>
</li>
</ol>
