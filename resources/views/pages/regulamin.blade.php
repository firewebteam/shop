<p align="CENTER"><span style="color: #000000;">§8 Rękojmia</span></p>
<p align="CENTER"> </p>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Dostarczenie Towaru w ramach realizacji wynikających z rękojmi za wady odbywa się na koszt Sprzedawcy.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Sprzedawca odpowiada z tytułu rękojmi, jeżeli wada zostanie stwierdzona przed upływem dwóch lat od dnia wydania Towaru Konsumentowi. Sprzedawca odpowiada wobec Konsumenta jeżeli Towar konsumpcyjny w chwili jego wydania był niezgodny z umową, posiada wady fizyczne, prawne. Sprzedawca odpowiada za niezgodność Towaru konsumpcyjnego z umową w przypadku jej stwierdzenia przed upływem dwóch lat od wydania tego Towaru Kupującemu, przy czym w razie wymiany Towaru termin ten biegnie na nowo. </span><strong><span style="color: #000000;">Wada fizyczna polega na niezgodności rzeczy sprzedanej z umową. </span></strong><span style="color: #000000;">W szczególności rzecz sprzedana jest niezgodna z umową, jeżeli:</span></p>
</li>
</ol>
<ol>
<li>
<p align="JUSTIFY"><span style="color: #000000;">nie ma właściwości, które rzecz tego rodzaju powinna mieć ze względu na cel w umowie oznaczony albo wynikający z okoliczności lub przeznaczenia;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">nie ma właściwości, o których istnieniu Sprzedawca zapewnił Kupującego, w tym przedstawiając próbkę lub wzór;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">nie nadaje się do celu, o którym Kupujący poinformował Sprzedawcę przy zawarciu umowy, a Sprzedawca nie zgłosił zastrzeżenia co do takiego jej przeznaczenia;</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">została Kupującemu wydana w stanie niezupełnym.</span></p>
</li>
</ol>
<ol start="3">
<li>
<p align="JUSTIFY"><span style="color: #000000;">Zgłoszenie o wadach Towaru należy przesłać drogą elektroniczną na adres mailowy Sprzedawcy</span><u> </u><span style="color: #000000;">lub w formie pisemnej na adres pocztowy Sprzedawcy (patrz §1 pkt 3.: </span><span style="color: #000000;">„Adres Sprzedawcy”</span><span style="color: #000000;">). Jeśli konsument ma trudności i nie wie jak skonstruować zgłoszenie o wadach Towaru, zgłoszenie może przesłać dla przykładu na formularzu stanowiącym załącznik nr 2 do niniejszego Regulaminu, co stanowi tylko i wyłącznie ułatwienie dla procesu reklamacji, nie stanowi żadnego wymogu korzystania z w/w wzorca dla skuteczności reklamacji.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Jeżeli jest to niezbędne dla prawidłowej oceny wad fizycznych Towaru, na prośbę i po ustaleniach wstępnych ze Sprzedawcą, Towar należy dostarczyć na adres siedziby Sprzedawcy (patrz §1 pkt 3.: </span><span style="color: #000000;">„Adres Sprzedawcy”</span><span style="color: #000000;">)</span><span style="color: #000000;"> gdy tylko właściwości Produktu na to pozwalają.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Sprzedawca odpowiada niezwłocznie na zgłoszenie Konsumenta jednak nie później niż w terminie do 14 dni kalendarzowych od dnia jego otrzymania. Nierozpatrzenie zgłoszenia w zakreślonym terminie jest równoznaczne z jego uwzględnieniem przez Sprzedawcę i uznaniem go za uzasadnione.</span></p>
</li>
<li>
<p align="JUSTIFY"><span style="color: #000000;">Sprzedawca pokrywa koszty odebrania Towaru, dostawy, usunięcia wad lub wady i wymiany Towaru na nowy. </span></p>
</li>
</ol>
